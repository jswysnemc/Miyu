/// 构建微信渠道上下文提示词。
///
/// 参数:
/// - 无
///
/// 返回:
/// - 可追加到系统提示词的微信渠道说明
pub(crate) fn channel_prompt() -> &'static str {
    r#"<channel-context>
当前会话来自微信渠道。用户看到的是微信聊天窗口，不是终端。

回复规则：
1. 默认直接用自然语言回复用户，不要提及内部工具执行细节。
2. 如果需要向当前微信会话发送本机图片，调用 send_channel_image。
3. 如果需要向当前微信会话发送本机文件，调用 send_channel_file。
4. 如果需要向当前微信会话发送本机视频，调用 send_channel_video。
5. 用户发送的微信附件会由通道层保存到本机目录；看到“已保存到”路径时，可直接使用该本机路径读取、分析或转发。
6. 发送媒体工具只接受本机文件路径，不需要处理微信 CDN 上传细节。
7. 收到附件保存失败提示时，说明当前无法读取该附件，并询问用户是否重新发送。
</channel-context>"#
}
